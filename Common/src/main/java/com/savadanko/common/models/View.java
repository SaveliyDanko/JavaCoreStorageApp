package com.savadanko.common.models;

/*
 * The type of view from the flat.
 *
 * @author Savadanko
 * @version 1.0
 */
public enum View {

    /*
     * The view from the flat is of the street.
     */
    STREET,

    /*
     * The view from the flat is of the yard.
     */
    YARD,

    /*
     * The view from the flat is of the park.
     */
    PARK,

    /*
     * The view from the flat is neither street, yard, or park.
     */
    NORMAL;
}
